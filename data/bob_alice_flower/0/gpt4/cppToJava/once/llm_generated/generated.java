public class Solution {
    public long flowerGame(int n, int m) {
        long AOdd = n % 2 != 0 ? n / 2 + 1 : n / 2;
        long AEven = n / 2;
        long BOdd = m % 2 != 0 ? m / 2 + 1 : m / 2;
        long BEven = m / 2;
        return AOdd * BEven + AEven * BOdd;
    }
}
